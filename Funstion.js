// Regular Function
function greet() {
  console.log("Hello, World!");
}

// Arrow Function
const add = (x, y) => x + y;

// Immediately Invoked Function Expression (IIFE)
(function() {
  console.log("This is an IIFE");
})();

// Named Function Expression
const multiply = function(x, y) {
  return x * y;
};
